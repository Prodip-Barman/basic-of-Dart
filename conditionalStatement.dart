void main() {
//if statement start

  int a = 10;

  if (a >= 5) {
    print("Ans is True");
  }

// if statement close

//if else statement start

  var salary = 25000;

  if (salary > 20000) {
    print("Congratulations! You got promotions");
  } else {
    print("Work Hard!");
  }
//if else statement stop

//if else if statement start

  var marks = 75;

  if (marks >= 80 && marks <= 100) {
    print("You got A+");
  } else if (marks <= 79 && marks >= 70) {
    print("You got A");
  } else if (marks <= 69 && marks >= 60) {
    print("You got B");
  } else {
    print("You failed");
  }
}
